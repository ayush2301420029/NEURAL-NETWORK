PATTERN_SIZE = GRID_SIZE * GRID_SIZE  # 100


def get_pattern_vector(letter):
    """Return flattened bipolar vector (-1/+1) for a letter."""
    return np.array(ALPHABET_PATTERNS[letter], dtype=np.float64).flatten()


class HopfieldNetwork:
    """
    Discrete Hopfield Network with Hebbian learning.
    Stores bipolar patterns (-1/+1) and recalls the closest stored pattern.
    """

    def __init__(self, size=PATTERN_SIZE):
        self.size = size
        self.weights = np.zeros((size, size))
        self.stored_patterns = {}

    def train(self, patterns_dict):
        """
        Train with Hebbian rule: W = (1/N) * sum(p * p^T) with zero diagonal.
        patterns_dict: {letter: bipolar_vector}
        """
        self.stored_patterns = dict(patterns_dict)
        self.weights = np.zeros((self.size, self.size))

        for label, pattern in patterns_dict.items():
            p = pattern.reshape(-1, 1)
            self.weights += p @ p.T

        self.weights /= len(patterns_dict)
        np.fill_diagonal(self.weights, 0)

    def recall(self, pattern, max_iterations=20):
        """
        Synchronous update until convergence.
        Returns: (recalled_pattern, energy_history, iteration_snapshots)
        """
        state = pattern.copy().astype(np.float64)
        energy_history = [self._energy(state)]
        snapshots = [state.copy()]

        for i in range(max_iterations):
            new_state = np.sign(self.weights @ state)
            new_state[new_state == 0] = 1  # break ties to +1
            energy_history.append(self._energy(new_state))
            snapshots.append(new_state.copy())

            if np.array_equal(new_state, state):
                break
            state = new_state

        return state, energy_history, snapshots

    def identify(self, recalled_pattern):
        """Find the stored pattern closest to the recalled state."""
        best_label = None
        best_similarity = -np.inf

        for label, stored in self.stored_patterns.items():
            similarity = np.dot(recalled_pattern, stored) / self.size
            if similarity > best_similarity:
                best_similarity = similarity
                best_label = label

        return best_label, float(best_similarity)

    def _energy(self, state):
        """Compute Hopfield energy: E = -0.5 * s^T W s"""
        return float(-0.5 * state @ self.weights @ state)

    def add_noise(self, pattern, noise_level=0.1):
        """Flip a fraction of bits randomly."""
        noisy = pattern.copy()
        n_flip = int(self.size * noise_level)
        indices = np.random.choice(self.size, n_flip, replace=False)
        noisy[indices] *= -1
        return noisy
